Mulher Maravilha
Descrição
Mulher-Maravilha é uma personagem fictícia de histórias em quadrinhos publicadas pela editora estadunidense DC Comics, originalmente é uma super-heroína de origem grego-romana, alter ego da princesa Diana de Themyscira, também conhecida como Diana Prince no Universo DC.
Embaixadora honorária da ONU é considerada um dos maiores ícones da cultura pop do sexo feminino da nona arte e ícone da cultura feminista.Sua primeira aventura foi na revista All Star Comics #8 de dezembro de 1941, nos Estados Unidos.

Observação
Escrita por William Moulton Marston e desenhada por H. G. Peter (Harry George Peter). A história tem continuação direta em Sensation Comics #1 de janeiro de 1942. Com o sucesso alcançado, ela ganhou sua própria revista em quadrinhos em maio de 1942, Wonder Woman #1, que foi transferida exclusivamente para a DC Comics em 1944.


Iron Man
Descrição
 Tony Stark, um industrialista e mestre em engenharia, constrói uma armadura superpoderosa e se torna um super-herói tecnologicamente avançado, o Homem de Ferro. 
Homem de Ferro (no original, Iron Man) é um filme americano de super-herói de 2008 baseado no personagem homônimo da Marvel Comics, produzido pela Marvel Studios e distribuído pela Paramount Pictures. É o primeiro filme do Universo Cinematográfico Marvel.


Observação
Dirigido por Jon Favreau, com um roteiro escrito por Mark Fergus & Hawk Ostby e Art Marcum & Matt Holloway. É estrelado por Robert Downey Jr., Terrence Howard, Jeff Bridges, Shaun Toub e Gwyneth Paltrow. 



Pantera Negra
Descrição
Black Panther é um filme de super-herói estadunidense de 2018, baseado no personagem de mesmo nome da Marvel Comics, produzido pela Marvel Studios e distribuído pela Walt Disney Studios Motion Pictures, sendo o décimo oitavo filme do Universo Cinematográfico Marvel, além de ser o primeiro título solo do personagem. 

Observação

Lançado em fevereiro de 2018 e dirigido por Ryan Coogler e Joe Robert Cole, é estrelado por Chadwick Boseman como TChalla / Pantera Negra, Michael B. Jordan, Lupita Nyongo, Martin Freeman, Daniel Kaluuya, Angela Bassett, Danai Gurira, Andy Serkis e Forest Whitaker. 
Em Black Panther, TChalla volta a casa como rei de Wakanda, mas encontra sua soberania desafiada por um adversário de longa data em um conflito que tem consequências globais.
